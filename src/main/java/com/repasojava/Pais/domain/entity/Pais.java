package com.repasojava.Pais.domain.entity;

public class Pais {
    private Integer id;
    private String name;

    
    public Pais() {
    }
    
    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    
}
